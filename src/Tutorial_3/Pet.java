package Tutorial_3;

public class Pet {

    String name;
    String price;
    int age;

    public Pet(String name, String price, int age){
        this.name = name;
        this.price = price;
        this. age = age;

    }

    public void setName(String name){
        this.name = name;
    }

    public void setPrice(String price){
        this.price = price;
    }

    public void setAge(int age){
        this.age = age;
    }
}
